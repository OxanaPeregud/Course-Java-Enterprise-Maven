package com.peregud.commandpatternver.receiver;

public class Radio {

    public void high() {
        System.out.println("Radio volume is high");
    }

    public void medium() {
        System.out.println("Radio volume is medium");
    }

    public void low() {
        System.out.println("Radio volume is low");
    }

    public void off() {
        System.out.println("Radio is off");
    }
}
